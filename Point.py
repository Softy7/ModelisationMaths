class Point:

    def __init__(self, x: float, y: float, z: float = 0):
        """
        Défini l'emplacement d'un point
        :param x: coordonnées x
        :param y: coordonnées y
        :param z: coordonnées z, la valeur est 0 si non spécifiée
        """
        self.x = x
        self.y = y
        self.z = z




